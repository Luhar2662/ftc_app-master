import org.firstinspires.ftc.teamcode.HardwarePushbot_RBZ_Mecanum;
/**
 * This file provides basic Telop driving for a Pushbot robot.
 * The code is structured as an Iterative OpMode
 *
 * This OpMode uses the common Pushbot hardware class to define the devices on the robot.
 * All device access is managed through the HardwarePushbot class.
 *
 * This particular OpMode executes a basic Tank Drive Teleop for a PushBot
 * It raises and lowers the claw using the Gampad Y and A buttons respectively.
 * It also opens and closes the claws slowly using the left and right Bumper buttons.
 *
 * Use Android Studios to Copy this Class, and Paste it into your team's code folder with a new name.
 * Remove or comment out the @Disabled line to add this opmode to the Driver Station OpMode list
 */

@TeleOp(name="Pushbot: Teleop Tank 2", group="Pushbot")
//@Disabled
public class PushbotTeleopTank_Iterative_RBZ_Mecanum extends OpMode{

    /* Declare OpMode members. */
    HardwarePushbot_RBZ_Mecanum robot       = new HardwarePushbot_RBZ_Mecanum(); // use the class created to define a Pushbot's hardware
                                                         // could also use HardwarePushbotMatrix class.
    double          clawOffset  = 0.0 ;                  // Servo mid position
    final double    CLAW_SPEED  = 0.02 ;                 // sets rate to move servo


    /*
     * Code to run ONCE when the driver hits INIT
     */
    @Override
    public void init() {
        /* Initialize the hardware variables.
         * The init() method of the hardware class does all the work here
         */
        robot.init(hardwareMap);

        // Send telemetry message to signify robot waiting;
        telemetry.addData("Say", "Hello Driver");    //
    }

    /*
     * Code to run REPEATEDLY after the driver hits INIT, but before they hit PLAY
     *\
    @Override
    public void init_loop() {

    }

    /*
     * Code to run ONCE when the driver hits PLAY
     */
    @Override
    public void start() {
    }

public void loop() {
    double r = Math.hypot(gamepad1.left_stick_x, gamepad1.left_stick_y);
    double robotAngle = Math.atan2(gamepad1.left_stick_y, gamepad1.left_stick_x) - Math.PI / 4;
    double rightX = gamepad1.right_stick_x;
    final double LeftFront = r * Math.cos(robotAngle) + rightX;
    final double RightFront = r * Math.sin(robotAngle) - rightX;
    final double LeftBack = r * Math.sin(robotAngle) + rightX;
    final double RightBack = r * Math.cos(robotAngle) - rightX;

    robot.leftMotorF.setPower(LeftFront);
    robot.rightMotorF.setPower(RightFront);
    robot.leftMotorB.setPower(LeftBack);
    robot.rightMotorB.setPower(RightBack);
}
    public void stop () {
    }
    }